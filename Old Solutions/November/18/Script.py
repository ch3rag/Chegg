# Given Dictionary
roman_numerals = {'I': 1, 'II': 2, 'III': 3, 'V': 5}

# Get All The Keys From Dictionary In A List
keys = list(roman_numerals.keys())

# Get All The Values From Dictionary In A List
values = list(roman_numerals.values())

# Get All The Items From Dictionary In A List
items = list(roman_numerals.items())

# Display List Of Keys
print(f'List of Keys: {keys}')
# Display List Of Values
print(f'List of Values: {values}')
# Display List Of Items
print(f'List of Items: {items}')
