# Input Integer
print('Enter integer (32 - 126):')
myint = int(input())

# Input Float
print('Enter float:')
myfloat = float(input())

# Input Character
print('Enter character:')
mychar = input()

# Input String
print('Enter string:')
mystring = input()

# Print All Values 
print(myint, myfloat, mychar, mystring)
print(mystring, mychar, myfloat, myint)

# Print Int Converted To Char
print(f'{myint} converted to a character is {chr(myint)}')
